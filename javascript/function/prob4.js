// Write 4 functions to perform addition, difference, multiplication, division.

function add() {
  let a = 10;
  let b = 20;
  console.log(a + b);
}

function diff() {
  let x = 10;
  let y = 20;
  console.log(x - y);
}

function mult() {
  let A = 10;
  let B = 20;
  console.log(A * B);
}

function div() {
  let X = 10;
  let Y = 20;
  console.log(X / Y);
}

div();
add();
diff();
mult();
